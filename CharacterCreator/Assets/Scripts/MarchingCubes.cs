    public struct Cube
    {
        // Corresponds to the 8 points of the cube
        private static readonly Vector3[] CUBE_POINT_OFFSETS = new Vector3[]
        {
            new Vector3(0,0,1),
            new Vector3(1,0,1),
            new Vector3(1,0,0),
            new Vector3(0,0,0),
            new Vector3(0,1,1),
            new Vector3(1,1,1),
            new Vector3(1,1,0),
            new Vector3(0,1,0)
        };

        private Vector3 cubeStartPoint;
        private Vector3[] gridPoints;
        private float gridSize;

        public float[] SurfaceValues
        {
            get; private set;
        }

        public Cube(Vector3 pt, float size, Func<Vector3, float> surfaceValueEvaluator)
        {
            cubeStartPoint = pt;
            gridSize = size;

            gridPoints = new Vector3[8];

            SurfaceValues = new float[8];
            for (int i = 0; i < 8; i++)
            {
                Vector3 offsets = CUBE_POINT_OFFSETS[i];

                SurfaceValues[i] = surfaceValueEvaluator(this[i]);
            }
        }

        public Vector3 this[int index]
        {
            get
            {
                Vector3 offsets = CUBE_POINT_OFFSETS[index];
                return new Vector3(cubeStartPoint.x + offsets.x * gridSize,
                                    cubeStartPoint.y + offsets.y * gridSize,
                                    cubeStartPoint.z + offsets.z * gridSize);
            }
        }
    }

